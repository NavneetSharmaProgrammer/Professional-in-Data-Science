# Python supports several built-in data types.
#      Integers (int) : whole numbers (e.g., 1, 2, 3)
#      Floats (float) : decimal numbers (e.g., 1.0, 2.5, 3.14)
#      Strings (str) : Text data enclosed in quotes (e.g., "Hello", 'World')
#      Booleans (bool) : Represents True or False values.
#      Lists : Ordered, mutable collections (e.g., [1, 2, 3], ["apple", "banana"])
#      Tuples : Ordered, immutable collections (e.g., (1, 2, 3), ("apple", "banana"))
#      Sets : Unordered collections of unique elements (e.g., {1, 2, 3}, {"apple", "banana"})
#       Dictionaries : Key-value pairs (e.g., {"name": "Alice", "age": 30})


age = 34# integer
print(age) 
print(type(age)) 

name = "Navneet" # string
print(name)
print(type(name))

cgpa = 9.8 # float
print(cgpa)
print(type(cgpa))

is_completed = True # boolean
print(is_completed)
print(type(is_completed))